public class Contoh1 {
    public static void main(String[]args) {
        // PERUALANAGAN FOR
        
        
        int x;
        for(x = 1; x <= 5; x++)
        {
            System.out.println("SMK Telkom Malang");
        }   
        
        //PERULANGAN WHILE
        int i = 1;
        while(i <= 5)
        {
            System.out.println("SMK Telkom Malanag");
            i++;
        }
        
        //PERULANAGAN DO WAHILE
        int j = 1;
        do
        {
            System.out.println("SMK Telkom Malang");
            j++;
            
        }
         while(j <= 5);       
    }
}
